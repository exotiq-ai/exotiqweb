// Generates public/sitemap.xml from the route manifest + blog frontmatter so
// the sitemap can never drift from the actual routes/content. Runs before the
// Vite build (Vite then copies public/ into dist/).

import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import {
  SITE_URL,
  STATIC_ROUTES,
  getBlogPostRoutes,
  getBlogTaxonomyRoutes,
} from './public-routes.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const PUBLIC_DIR = join(__dirname, '..', 'public');
const today = new Date().toISOString().slice(0, 10);

function toUrl(route) {
  // Emit trailing-slash locs so every entry resolves 200 with no redirect
  // (Netlify serves directory-style files and 301s the no-slash form).
  const path =
    route.path === '/'
      ? '/'
      : route.path.endsWith('/')
        ? route.path
        : `${route.path}/`;
  const loc = `${SITE_URL}${path}`;
  const lastmod = route.lastmod || today;
  return [
    '  <url>',
    `    <loc>${loc}</loc>`,
    `    <lastmod>${lastmod}</lastmod>`,
    `    <changefreq>${route.changefreq}</changefreq>`,
    `    <priority>${route.priority.toFixed(2)}</priority>`,
    '  </url>',
  ].join('\n');
}

function buildSitemap() {
  const routes = [
    ...STATIC_ROUTES,
    ...getBlogPostRoutes(),
    ...getBlogTaxonomyRoutes(),
  ];

  const body = routes.map(toUrl).join('\n');

  return `<?xml version="1.0" encoding="UTF-8"?>
<!-- Generated by scripts/generate-seo-artifacts.mjs — do not edit by hand. -->
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9"
        xmlns:xhtml="http://www.w3.org/1999/xhtml"
        xmlns:image="http://www.google.com/schemas/sitemap-image/1.1">
${body}
</urlset>
`;
}

function main() {
  const xml = buildSitemap();
  const outPath = join(PUBLIC_DIR, 'sitemap.xml');
  writeFileSync(outPath, xml, 'utf8');
  const count = (xml.match(/<url>/g) || []).length;
  // eslint-disable-next-line no-console
  console.log(`[seo] Wrote ${outPath} with ${count} URLs.`);
}

main();
